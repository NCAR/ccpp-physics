       ipat = (/ 5, 5, 4, 4, 3, 3, 2, 2, 1, 1, 1, 1, 1, 5 /)

contains
  ! #########################################################################################
  ! rrtmg_sw_cloud_optics
  ! #########################################################################################
  subroutine rrtmg_sw_cloud_optics(ncol, nlay, nBandsSW, cld_lwp, cld_ref_liq, cld_iwp,     &
       cld_ref_ice, cld_rwp, cld_ref_rain, cld_swp, cld_ref_snow, cld_frac, iswcliq,        &
       iswcice, tau_cld, ssa_cld, asy_cld, tau_precip, ssa_precip, asy_precip)
    ! Inputs
    integer,intent(in) :: &
         nBandsSW,     & ! Number of spectral bands
         ncol,         & ! Number of horizontal gridpoints
         nlay,         & ! Number of vertical layers
         iswcliq,      & !
         iswcice         !
    real(kind_phys), dimension(ncol,nlay), intent(in) :: &
         cld_frac,     & ! Cloud-fraction                         (1)
         cld_lwp,      & ! Cloud liquid water path                (g/m2)
         cld_ref_liq,  & ! Effective radius (liquid)              (micron)
         cld_iwp,      & ! Cloud ice water path                   (g/m2)
         cld_ref_ice,  & ! Effective radius (ice)                 (micron)
         cld_rwp,      & ! Cloud rain water path                  (g/m2)
         cld_ref_rain, & ! Effective radius (rain-drop)           (micron)
         cld_swp,      & ! Cloud snow-water path                  (g/m2)
         cld_ref_snow    ! Effective radius (snow-flake)          (micron)
    
    ! Outputs
    real(kind_phys),dimension(ncol,nlay,nBandsSW),intent(out) :: &
         tau_cld,      & ! In-cloud optical depth                  (1)
         ssa_cld,      & ! In-cloud single-scattering albedo       (1)
         asy_cld,      & ! In-cloud asymmetry parameter            (1) 
         tau_precip,   & ! Precipitation optical depth             (1)
         ssa_precip,   & ! Precipitation single-scattering albedo  (1)
         asy_precip      ! Precipitation asymmetry parameter       (1) 

    ! Local variables
    integer :: iCol, iLay, iBand, index, ia
    real(kind_phys) :: tau_rain, tau_snow, factor, fint, cld_ref_iceTemp,asyw,ssaw,za1,za2
         
    real(kind_phys), dimension(nBandsSW) :: ssa_rain, ssa_snow, asy_rain, asy_snow, &
         tau_liq, ssa_liq, asy_liq, tau_ice, ssa_ice, asy_ice, asycoliq,   &
         forwice, extcoice, asycoice, ssacoice, fdelta, extcoliq, ssacoliq

    ! Initialize
    tau_cld(:,:,:)    = 0._kind_phys
    ssa_cld(:,:,:)    = 1._kind_phys
    asy_cld(:,:,:)    = 0._kind_phys
    tau_precip(:,:,:) = 0._kind_phys
    ssa_precip(:,:,:) = 1._kind_phys
    asy_precip(:,:,:) = 0._kind_phys
    
    ! Compute cloud/precipitation radiative properties
    if (iswcliq > 0) then
       do iCol=1,ncol
          do iLay=1,nlay
             ! Initialize
             tau_liq(:)  = 0._kind_phys
             tau_ice(:)  = 0._kind_phys
             tau_rain    = 0._kind_phys
             tau_snow    = 0._kind_phys
             ssa_liq(:)  = 0._kind_phys
             ssa_ice(:)  = 0._kind_phys
             ssa_rain(:) = 0._kind_phys
             ssa_snow(:) = 0._kind_phys
             asy_liq(:)  = 0._kind_phys
             asy_ice(:)  = 0._kind_phys
             asy_rain(:) = 0._kind_phys
             asy_snow(:) = 0._kind_phys
             if (cld_frac(iCol,iLay) .gt. 1.e-12_kind_phys) then
                ! ###########################################################################
                ! Rain clouds
                ! ###########################################################################
                ! Rain optical depth (No band dependence)
                tau_rain = cld_rwp(iCol,iLay)*a0r

                ! Rain single-scattering albedo and asymmetry (Band dependent)
                do iBand=1,nBandsSW
                   ssa_rain(iBand) = tau_rain*(1.-b0r(iBand))
                   asy_rain(iBand) = ssa_rain(iBand)*c0r(iBand)
                enddo

                ! ###########################################################################
                ! Snow clouds
                ! ###########################################################################
                ! Snow optical depth (No band dependence)
                if (cld_swp(iCol,iLay) .gt. 0. .and. cld_ref_snow(iCol,iLay) .gt. 10._kind_phys) then
                   tau_snow = cld_swp(iCol,iLay)*1.09087*(a0s + a1s/(1.0315*cld_ref_snow(iCol,iLay)))     ! fu's formula 
                else
                   tau_snow = 0._kind_phys
                endif

                ! Snow single-scattering albedo and asymmetry (Band dependent)
                do iBand=1,nBandsSW
                   ssa_snow(iBand) = tau_snow*(1.-(b0s(iBand)+b1s(iBand)*1.0315*cld_ref_snow(iCol,iLay)))
                   asy_snow(iBand) = ssa_snow(iBand)*c0s(iBand)
                enddo

                ! ###########################################################################
                ! Liquid clouds
                ! ###########################################################################
                if (cld_lwp(iCol,iLay) .gt. 0) then
                   ! Find index in coefficient LUT for corresponding partice size.
                   factor = cld_ref_liq(iCol,iLay) - 1.5
                   index  = max( 1, min( 57, int( factor ) ))
                   fint   = factor - float(index)

                   ! Extract coefficents for all bands and compute radiative properties
                   do iBand=1,nBandsSW
                      ! Interpolate coefficients
                      if ( iswcliq == 1 ) then
                         extcoliq(iBand) = max(0._kind_phys, extliq1(index,iBand) + &
                              fint*(extliq1(index+1,iBand)-extliq1(index,iBand)))
                         ssacoliq(iBand) = max(0._kind_phys, min(1._kind_phys, ssaliq1(index,iBand) + &
                              fint*(ssaliq1(index+1,iBand)-ssaliq1(index,iBand))))
                         asycoliq(iBand) = max(0._kind_phys, min(1._kind_phys, asyliq1(index,iBand) + &
                              fint*(asyliq1(index+1,iBand)-asyliq1(index,iBand))))
                      elseif ( iswcliq == 2 ) then   ! use updated coeffs
                         extcoliq(iBand) = max(0._kind_phys, extliq2(index,iBand) + &
                              fint*(extliq2(index+1,iBand)-extliq2(index,iBand)))
                         ssacoliq(iBand) = max(0._kind_phys, min(1._kind_phys, ssaliq2(index,iBand) + &
                              fint*(ssaliq2(index+1,iBand)-ssaliq2(index,iBand))))
                         asycoliq(iBand) = max(0._kind_phys, min(1._kind_phys, asyliq2(index,iBand) + &
                              fint*(asyliq2(index+1,iBand)-asyliq2(index,iBand))))
                      endif
                      if (fint .lt. 0._kind_phys .and. ssacoliq(iBand) .gt. 1._kind_phys) then
                         ssacoliq(iBand) = ssaliq1(index,iBand)
                      endif
                      tau_liq(iBand) = cld_lwp(iCol,iLay) * extcoliq(iBand)
                      ssa_liq(iBand) = tau_liq(iBand)     * ssacoliq(iBand)
                      asy_liq(iBand) = ssa_liq(iBand)     * asycoliq(iBand)
                   enddo
                endif  ! IF cloudy with liquid condensate

                ! ###########################################################################
                ! Ice clouds
                ! ###########################################################################
                if (cld_iwp(iCol,iLay) .gt. 0) then
                   ! Ebert and curry approach for all particle sizes though somewhat
                   !  unjustified for large ice particles.
                   if ( iswcice == 1 ) then
                      cld_ref_iceTemp = min(130._kind_phys, max(13._kind_phys,cld_ref_ice(iCol,iLay)))
                      do iBand=1,nBandsSW
                         ia = ipat(iBand)           ! eb_&_c band index for ice cloud coeff
                         extcoice(iBand) = abari(ia) + bbari(ia) / cld_ref_iceTemp
                         ssacoice(iBand) = 1._kind_phys - cbari(ia) - dbari(ia)*cld_ref_iceTemp
                         asycoice(iBand) = ebari(ia)+fbari(ia)*cld_ref_iceTemp
                         tau_ice(iBand)  = cld_iwp(iCol,iLay) * extcoice(iBand)
                         ssa_ice(iBand)  = tau_ice(iBand)     * ssacoice(iBand)
                         asy_ice(iBand)  = ssa_ice(iBand)     * asycoice(iBand)
                      enddo

                   ! Streamer approach for ice effective radius between 5.0 and 131.0 microns.
                   elseif ( iswcice == 2 ) then
                      cld_ref_iceTemp = min(131._kind_phys, max(5.0_kind_phys,cld_ref_ice(iCol,iLay)))
                      factor = (cld_ref_iceTemp - 2.) / 3.
                      index  = max( 1, min( 42, int( factor ) ))
                      fint   = factor - float(index)
                      do iBand = 1,nBandsSW
                         extcoice(iBand) = extice2(index,iBand) + &
                              fint*(extice2(index+1,iBand)-extice2(index,iBand))
                         ssacoice(iBand) = ssaice2(index,iBand) + &
                              fint*(ssaice2(index+1,iBand)-ssaice2(index,iBand))
                         asycoice(iBand) = asyice2(index,iBand) + &
                              fint*(asyice2(index+1,iBand)-asyice2(index,iBand))
                         tau_ice(iBand) = cld_iwp(iCol,iLay) * extcoice(iBand)
                         ssa_ice(iBand) = tau_ice(iBand)     * ssacoice(iBand)
                         asy_ice(iBand) = ssa_ice(iBand)     * asycoice(iBand)
                      enddo

                   ! Fu's approach for ice effective radius between 4.8 and 135 microns
                   !  (generalized effective size from 5 to 140 microns).
                   ! https://doi.org/10.1175/1520-0442(1996)009<2058:AAPOTS>2.0.CO;2
                   elseif ( iswcice == 3 ) then
                      cld_ref_iceTemp = max( 5.0, min( 140.0, 1.0315*cld_ref_ice(iCol,iLay) ))
                      ! Determine indices for table interpolation.
                      factor = (cld_ref_iceTemp - 2._kind_phys) / 3._kind_phys
                      index  = max( 1, min( 45, int( factor ) ))
                      fint   = factor - float(index)
                      do iBand = 1,nBandsSW
                         ! Interpolate coefficient tables to appropriate ice-particle size.
                         extcoice(iBand) = max(0._kind_phys, extice3(index,iBand) + &
                              fint*(extice3(index+1,iBand)-extice3(index,iBand)))                       ! eq (3.9a)
                         ssacoice(iBand) = max(0._kind_phys, min(1._kind_phys, ssaice3(index,iBand) + &
                              fint*(ssaice3(index+1,iBand)-ssaice3(index,iBand))))                      ! eq (3.9b)
                         asycoice(iBand) = max(0._kind_phys, min(1._kind_phys, asyice3(index,iBand) + &
                              fint*(asyice3(index+1,iBand)-asyice3(index,iBand))))                      ! eq (3.9c)
                         fdelta(iBand)   = fdlice3(index,iBand) + &
                              fint*(fdlice3(index+1,iBand)-fdlice3(index,iBand))                        ! eq (3.9d)
                         forwice(iBand) = fdelta(iBand) + 0.5_kind_phys / ssacoice(iBand)
                         if (forwice(iBand) .gt. asycoice(iBand)) forwice(iBand) = asycoice(iBand)
                         tau_ice(iBand) = cld_iwp(iCol,iLay) * extcoice(iBand)
                         ssa_ice(iBand) = tau_ice(iBand)     * ssacoice(iBand)
                         asy_ice(iBand) = ssa_ice(iBand)     * asycoice(iBand)
                      enddo
                   endif
                endif  ! IF cloudy column with ice condensate
             endif     ! IF cloudy column

             ! ###########################################################################
             ! Compute total cloud and precipitation radiative properties (tau, omega, and g)
             ! ###########################################################################
             if (cld_frac(iCol,iLay) .gt. 1.e-12_kind_phys) then
                do iBand = 1,nBandsSW
                   !
                   ! Cloud optics
                   !
                   ! Sum up radiative properties by type.
                   tau_cld(iCol,iLay,iBand) = max(1.e-12_kind_phys, tau_liq(iBand) + tau_ice(iBand))
                   ssa_cld(iCol,iLay,iBand) = max(1.e-12_kind_phys, ssa_liq(iBand) + ssa_ice(iBand))
                   asy_cld(iCol,iLay,iBand) = max(1.e-12_kind_phys, asy_liq(iBand) + asy_ice(iBand))
                   ! Combine
                   asyw = asy_cld(iCol,iLay,iBand)/max(1.e-12_kind_phys, ssa_cld(iCol,iLay,iBand))
                   ssaw = min(1._kind_phys-0.000001, ssa_cld(iCol,iLay,iBand)/tau_cld(iCol,iLay,iBand))
                   za1  = asyw * asyw
                   za2  = ssaw * za1
                   tau_cld(iCol,iLay,iBand) = (1._kind_phys - za2) * tau_cld(iCol,iLay,iBand) 
                   ssa_cld(iCol,iLay,iBand) = (ssaw - za2) / (1._kind_phys - za2)
                   asy_cld(iCol,iLay,iBand) = asyw/(1+asyw)
                   !
                   ! Precipitation optics 
                   !
                   ! Sum up radiative properties by type.
                   tau_precip(iCol,iLay,iBand) = max(1.e-12_kind_phys, tau_rain        + tau_snow)
                   ssa_precip(iCol,iLay,iBand) = max(1.e-12_kind_phys, ssa_rain(iBand) + ssa_snow(iBand))
                   asy_precip(iCol,iLay,iBand) = max(1.e-12_kind_phys, asy_rain(iBand) + asy_snow(iBand))
                   ! Combine
                   asyw = asy_precip(iCol,iLay,iBand)/max(1.e-12_kind_phys, ssa_precip(iCol,iLay,iBand))
                   ssaw = min(1._kind_phys-0.000001, ssa_precip(iCol,iLay,iBand)/tau_precip(iCol,iLay,iBand))
                   za1  = asyw * asyw
                   za2  = ssaw * za1
                   tau_precip(iCol,iLay,iBand) = (1._kind_phys - za2) * tau_precip(iCol,iLay,iBand) 
                   ssa_precip(iCol,iLay,iBand) = (ssaw - za2) / (1._kind_phys - za2)
                   asy_precip(iCol,iLay,iBand) = asyw/(1+asyw)                   				

                enddo  ! Loop over SW bands
             endif     ! END sum cloudy properties
             !
          enddo        ! Loop over layers
       enddo           ! Loop over columns
    endif
  end subroutine rrtmg_sw_cloud_optics
