       ebari = (/ 7.661e-01,7.730e-01,7.865e-01,8.172e-01,9.595e-01 /), &
       fbari = (/ 5.851e-04,5.665e-04,7.204e-04,7.463e-04,1.076e-04 /)

  ! ipat is bands index for ebert & curry ice cloud (for iflagice=1)
